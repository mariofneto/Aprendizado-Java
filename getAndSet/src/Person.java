
public class Person {
	
	private String name;
	private int idade;
	
	public String getName(){
		return this.name;
	}
	
	public int getIdade() {
		return this.idade;
	}
	
	public void setName(String newName) {
		this.name = newName;
	}
	
	public void setIdade(int newIdade) {
		this.idade = newIdade;
	}
}
